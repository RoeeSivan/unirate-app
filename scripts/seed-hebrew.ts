async function main() {
  let updated = 0
  for (const [englishTitle, data] of Object.entries(hebrewData)) {
    try {
      const result = await prisma.course.updateMany({
        where: { title: englishTitle },
        data: { titleHe: data.titleHe, descriptionHe: data.descriptionHe },
      })
      if (result.count > 0) {
        console.log(`Updated: ${englishTitle} -> ${data.titleHe}`)
        updated++
      } else {
        console.log(`Not found: ${englishTitle}`)
      }
    } catch (err) {
      console.error(`Error updating ${englishTitle}:`, err)
    }
  }
  console.log(`\nDone. Updated ${updated} courses.`)
}

main().finally(() => prisma.$disconnect())
